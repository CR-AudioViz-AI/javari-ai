// lib/repair/pullRequestCreator.ts
// Purpose: GitHub PR automation — creates a repair branch, commits the patched
//          file, opens a pull request with full context, and optionally auto-merges
//          safe changes directly to main.
// Date: 2026-03-07

import { getSecret }       from "@/lib/platform-secrets/getSecret";
import type { PatchResult } from "./patchGenerator";
import type { RepairPlan }  from "./repairPlanner";

// ── Types ──────────────────────────────────────────────────────────────────

export interface PRResult {
  ok          : boolean;
  mode        : "direct_commit" | "pull_request" | "skipped";
  commitSha?  : string;
  prUrl?      : string;
  prNumber?   : number;
  branch?     : string;
  error?      : string;
}

// ── GitHub REST helpers ────────────────────────────────────────────────────

async function githubToken(): Promise<string> {
  try { const t = await getSecret("GITHUB_TOKEN"); if (t) return t; } catch {}
  return process.env.GITHUB_TOKEN ?? process.env.GH_PAT ?? "";
}

async function ghAPI<T = unknown>(
  method : string,
  path   : string,
  body?  : unknown,
  token  : string = ""
): Promise<T> {
  const headers: Record<string, string> = {
    Accept        : "application/vnd.github.v3+json",
    "User-Agent"  : "javari-repair/1.0",
    "Content-Type": "application/json",
  };
  if (token) headers.Authorization = `token ${token}`;

  const res = await fetch(`https://api.github.com/${path}`, {
    method,
    headers,
    body  : body ? JSON.stringify(body) : undefined,
    signal: AbortSignal.timeout(20_000),
  });
  const text = await res.text();
  if (!res.ok) throw new Error(`GitHub API ${res.status}: ${text.slice(0, 200)}`);
  return JSON.parse(text) as T;
}

/** Get SHA of branch tip */
async function getBranchSHA(repo: string, branch: string, token: string): Promise<string> {
  const ref = await ghAPI<{ object: { sha: string } }>(
    "GET", `repos/${repo}/git/ref/heads/${branch}`, undefined, token
  );
  return ref.object.sha;
}

/** Create a new branch from a base SHA */
async function createBranch(
  repo     : string,
  branch   : string,
  fromSHA  : string,
  token    : string
): Promise<void> {
  await ghAPI("POST", `repos/${repo}/git/refs`, {
    ref: `refs/heads/${branch}`,
    sha: fromSHA,
  }, token);
}

/** Get file SHA for update (needed for PUT) */
async function getFileSHA(
  repo  : string,
  path  : string,
  branch: string,
  token : string
): Promise<string | undefined> {
  try {
    const d = await ghAPI<{ sha?: string }>(
      "GET", `repos/${repo}/contents/${encodeURIComponent(path)}?ref=${branch}`,
      undefined, token
    );
    return d.sha;
  } catch {
    return undefined;
  }
}

/** Commit a file to a branch */
async function commitFile(
  repo    : string,
  path    : string,
  content : string,
  message : string,
  branch  : string,
  token   : string
): Promise<string> {
  const sha     = await getFileSHA(repo, path, branch, token);
  const encoded = Buffer.from(content).toString("base64");
  const payload: Record<string, unknown> = { message, content: encoded, branch };
  if (sha) payload.sha = sha;

  const result = await ghAPI<{ commit: { sha: string } }>(
    "PUT", `repos/${repo}/contents/${encodeURIComponent(path)}`, payload, token
  );
  return result.commit.sha;
}

/** Create a pull request */
async function createPR(
  repo    : string,
  title   : string,
  body    : string,
  head    : string,
  base    : string,
  token   : string
): Promise<{ url: string; number: number }> {
  const pr = await ghAPI<{ html_url: string; number: number }>(
    "POST", `repos/${repo}/pulls`, { title, body, head, base }, token
  );
  return { url: pr.html_url, number: pr.number };
}

/** Add labels to a PR */
async function addLabels(
  repo    : string,
  prNumber: number,
  labels  : string[],
  token   : string
): Promise<void> {
  try {
    await ghAPI("POST", `repos/${repo}/issues/${prNumber}/labels`, { labels }, token);
  } catch { /* labels are best-effort */ }
}

// ── PR body builder ────────────────────────────────────────────────────────

function buildPRBody(plan: RepairPlan, patch: PatchResult): string {
  const issue = plan.issue;
  return `## 🔧 Javari Autonomous Repair

**File:** \`${issue.file}\`
**Severity:** \`${issue.severity}\`
**Type:** \`${issue.type}\`
**Rule:** \`${(issue as { rule?: string }).rule ?? "general"}\`
**Strategy:** \`${plan.strategy}\`
**Risk Level:** \`${plan.riskLevel}\`

### Issue
${issue.description}${issue.line ? ` (line ${issue.line})` : ""}

### Fix Applied
${issue.suggested_fix}

### Patch Summary
${patch.patchSummary}

### Steps Taken
${plan.steps.map(s => `${s.order}. **${s.action}**: ${s.description}`).join("\n")}

---
*Generated by Javari Code Intelligence Engine — automated repair*
*Task: ${plan.taskId ?? "direct"} | Engine: repair_code executor*`;
}

// ── Main PR creator ────────────────────────────────────────────────────────

export async function createRepairPR(
  patch      : PatchResult,
  repo       : string,
  baseBranch : string = "main"
): Promise<PRResult> {
  if (!patch.ok) {
    return { ok: false, mode: "skipped", error: `Patch generation failed: ${patch.error}` };
  }

  // If content is unchanged, nothing to do
  if (patch.patchedContent === patch.originalContent && !patch.newFilePath) {
    return { ok: true, mode: "skipped", error: "Content unchanged — no commit needed" };
  }

  const token = await githubToken();
  const plan  = patch.plan;

  try {
    if (plan.canAutoMerge && !plan.requiresPR) {
      // Safe change — commit directly to main (remove_dead_code, add_comment_only)
      const filePath = patch.newFilePath ?? plan.targetFile;
      const commitMsg = `[javari-repair] ${plan.strategy}: ${plan.issue.description.slice(0, 60)} (${plan.issue.file})`;
      const sha = await commitFile(repo, filePath, patch.patchedContent, commitMsg, baseBranch, token);

      return { ok: true, mode: "direct_commit", commitSha: sha, branch: baseBranch };
    }

    // All other changes — create a PR branch
    const baseSHA = await getBranchSHA(repo, baseBranch, token);
    const branch  = plan.prBranch.replace(/[^a-z0-9/_-]/gi, "-").slice(0, 100);

    // Create the repair branch
    await createBranch(repo, branch, baseSHA, token);

    // Commit the patched file to the branch
    const filePath  = patch.newFilePath ?? plan.targetFile;
    const commitMsg = `[javari-repair] ${plan.strategy}: fix ${plan.issue.severity} ${plan.issue.type} in ${plan.issue.file}`;
    const sha = await commitFile(repo, filePath, patch.patchedContent, commitMsg, branch, token);

    // Create the pull request
    const prTitle = `[Javari Repair] [${plan.issue.severity.toUpperCase()}] ${plan.issue.description.slice(0, 70)}`;
    const prBody  = buildPRBody(plan, patch);
    const pr      = await createPR(repo, prTitle, prBody, branch, baseBranch, token);

    // Add labels
    await addLabels(repo, pr.number, [
      "javari-repair",
      plan.issue.severity,
      plan.issue.type,
      plan.strategy,
    ], token);

    return {
      ok        : true,
      mode      : "pull_request",
      commitSha : sha,
      prUrl     : pr.url,
      prNumber  : pr.number,
      branch,
    };

  } catch (err) {
    return { ok: false, mode: "skipped", error: String(err) };
  }
}
